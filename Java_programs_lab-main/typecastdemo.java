// Q22) wajp to demonstrate typecasting

public class typecastdemo {
  
        public static void main(String[] args) {
            // Implicit typecasting (widening)
            int x = 10;
            double y = x;
            System.out.println(y);
    
            // Explicit typecasting (narrowing)
            double a = 3.14;
            int b = (int) a;
            System.out.println(b);
        }
    
    
    
}
