// Q5) program for multiple of 7

public class multiple7 {
    public static void main ( String args [])
    {
        int m = 38;
        if ( m % 7 == 0)
        {
            System.out.println(m + " is a multiple of 7");
        }
        else {
            System.out.println(m + " is not a multiple of 7");
        }
    }
    
}
