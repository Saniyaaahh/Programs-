# Explain built in data types in python with example.

# 1 >> String data type:
# The string is a sequence of characters
# Ex :
a = "Aziz"
print(a)

# 2 >> List data type:
# Lists are used to store multiple items in a single variable.
# EX:
b = [1,2,3,4,5,6]
print(b)

# 3 >> Tuple Data Type:
# The tuple is another data type which is a sequence of data similar to a list.
# But it is immutable.
# Ex:
c=(1,2,3,4)
print(c)

# 4 >> Dictionary Data Type:
# Python Dictionary is an unordered sequence of data of key-value pair form.
# Ex:
d = {38:'Miran', 29:'Aziz',54:'Azeem'}
print(d)
